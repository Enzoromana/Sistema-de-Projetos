CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- 1. Profiles Table
CREATE TABLE IF NOT EXISTS public.profiles (
  id UUID REFERENCES auth.users ON DELETE CASCADE PRIMARY KEY,
  email TEXT UNIQUE NOT NULL,
  full_name TEXT,
  role TEXT DEFAULT 'user' CHECK (role IN ('user', 'admin')),
  is_approved BOOLEAN DEFAULT false,
  access_projects BOOLEAN DEFAULT false,
  access_rooms BOOLEAN DEFAULT false,
  access_audit BOOLEAN DEFAULT false,
  access_medical BOOLEAN DEFAULT false,
  cpf TEXT,
  birth_date DATE,
  setor TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 2. Projects & Tasks
CREATE TABLE IF NOT EXISTS public.projects (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  name text not null,
  description text,
  status text check (status in ('pending', 'progress', 'done', 'blocked')) default 'pending',
  priority text check (priority in ('low', 'medium', 'high')) default 'medium',
  deadline date,
  diagram_code text -- For DevCockpit
);

CREATE TABLE IF NOT EXISTS public.tasks (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  project_id bigint references public.projects(id) on delete cascade not null,
  title text not null,
  status text check (status in ('pending', 'done', 'progress', 'blocked')) default 'pending',
  position integer default 0,
  start_date date,
  deadline date,
  completed_at timestamp with time zone
);

CREATE TABLE IF NOT EXISTS public.subtasks (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  task_id bigint references public.tasks(id) on delete cascade not null,
  title text not null,
  status text check (status in ('pending', 'done')) default 'pending',
  assignee text,
  position integer default 0,
  completed_at timestamp with time zone
);

-- 3. Room Bookings
